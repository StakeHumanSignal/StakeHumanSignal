// Reviewer pool — index = addrHash(address) % 4
// ─────────────────────────────────────────────────────────────────────────────
export const REVIEWER_POOL = [
  { defs: defsSpider,   name: "SPIDER-CLAW"  },
  { defs: defsScorpion, name: "SCORPION"     },
  { defs: defsDrill,    name: "DRILL-CLAW"   },
  { defs: defsMulti,    name: "MULTI-CLAW"   },
] as const;

// Deterministic hash: wallet address → pool index
export function addrHash(addr: string): number {
  let h = 0;
  for (let i = 0; i < addr.length; i++)
    h = (Math.imul(31, h) + addr.charCodeAt(i)) | 0;
  return Math.abs(h) % REVIEWER_POOL.length;
}
